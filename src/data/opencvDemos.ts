/**
 * OpenCV Playground — デモ機能カタログ（ソース・オブ・トゥルース）
 *
 * 72 件のデモ候補を定義。`categoryId` は opencvCategories.ts の id を参照する。
 *
 * 重要な方針:
 * - OpenCV 本体の機能と「OpenCV.js でブラウザ実装できるか(opencvJsSupport)」は独立に扱う。
 * - 確証がないものは安易に 'supported' にせず 'likely-supported' / 'needs-investigation' を使う。
 * - contrib 依存は OpenCvFunctionRef.isContrib と OpenCvModule の "-contrib" で示す。
 *
 * 型の正典は docs/spec/data-model.md。ここでは実装で使えるよう型も同梱する。
 */

import type { DemoPhase, DemoPriority, OpenCvModule } from './opencvCategories';

export type DemoDifficulty = 'beginner' | 'intermediate' | 'advanced';

export type DemoInputType =
  | 'image'
  | 'multiple-images'
  | 'video'
  | 'camera'
  | 'drawing'
  | 'sample-only';

export type DemoOutputType =
  | 'image'
  | 'mask'
  | 'overlay'
  | 'chart'
  | 'numeric'
  | 'text'
  | 'geometry'
  | 'video';

export type OpenCvJsSupportStatus =
  | 'supported'
  | 'likely-supported'
  | 'needs-investigation'
  | 'not-supported'
  | 'not-suitable-for-browser';

export interface OpenCvFunctionRef {
  name: string;
  module: OpenCvModule;
  isContrib?: boolean;
  docUrl?: string;
}

export interface DemoParameter {
  id: string;
  nameJa: string;
  nameEn: string;
  control: 'slider' | 'number' | 'select' | 'toggle' | 'color' | 'point' | 'kernel-size';
  description: string;
  default: number | string | boolean | number[];
  min?: number;
  max?: number;
  step?: number;
  options?: { label: string; value: string | number }[];
  opencvArg?: string;
}

export interface CodeExample {
  language: 'javascript' | 'python' | 'cpp';
  code: string;
  caption?: string;
}

export interface SampleImageRef {
  id: string;
  label: string;
  path?: string;
}

export interface OpenCvDemo {
  id: string;
  slug: string;
  titleJa: string;
  titleEn: string;
  categoryId: string;
  subcategory: string;
  description: string;
  longDescription?: string;
  useCases: string[];
  opencvFunctions: OpenCvFunctionRef[];
  opencvModules: OpenCvModule[];
  inputTypes: DemoInputType[];
  outputTypes: DemoOutputType[];
  parameters: DemoParameter[];
  sampleImages?: SampleImageRef[];
  difficulty: DemoDifficulty;
  priority: DemoPriority;
  phase: DemoPhase;
  isMvp: boolean;
  opencvJsSupport: OpenCvJsSupportStatus;
  browserSupportNotes?: string;
  implementationNotes: string;
  limitations: string;
  codeExample: CodeExample;
}

